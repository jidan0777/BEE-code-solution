#include<bits/stdc++.h>
using namespace std;
int main()
{
    float a,b;
    cin>>a>>b;
    double x,y;
    cin>>x>>y;


    cout<<setprecision(6)<<fixed;
    cout<<"A = "<<a<<", "<<"B = "<<b<<endl;
    cout<<"C = "<<x<<", "<<"D = "<<y<<endl;

    cout<<setprecision(1)<<fixed;
    cout<<"A = "<<a<<", "<<"B = "<<b<<endl;
    cout<<"C = "<<x<<", "<<"D = "<<y<<endl;
    cout<<setprecision(2)<<fixed;
    cout<<"A = "<<a<<", "<<"B = "<<b<<endl;
    cout<<"C = "<<x<<", "<<"D = "<<y<<endl;
    cout<<setprecision(3)<<fixed;
    cout<<"A = "<<a<<", "<<"B = "<<b<<endl;
    cout<<"C = "<<x<<", "<<"D = "<<y<<endl;


    cout<<setprecision(3)<<scientific;
    cout<<setiosflags(ios::uppercase);
    cout<<"A = "<<a<<", "<<"B = "<<b<<endl;
    cout<<"C = "<<x<<", "<<"D = "<<y<<endl;


    cout<<setprecision(0)<<fixed;
    cout<<"A = "<<a<<", "<<"B = "<<b<<endl;
    cout<<"C = "<<x<<", "<<"D = "<<y<<endl;


    return 0;
}

